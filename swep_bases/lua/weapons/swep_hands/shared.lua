/*---------------------------------------------------------
   Name: SWEP:Deploy( )
   Desc: Whip it out
---------------------------------------------------------*/
function SWEP:Deploy()

	local pPlayer = self.Owner;

	if ( !pPlayer ) then
		return;
	end

	self.Weapon:SendWeaponAnim( ACT_VM_DRAW )
	self:SetDeploySpeed( self.Weapon:SequenceDuration() )

	if ( !CLIENT ) then
		pPlayer:EquipSuit()
	end

	return true

end


